public class Number {

    private int valor; // 1 - esse atributo "valor" 

    public Number () { // 3 - parâmetro valor
        this.valor=0;  // 2 - recebe o valor do parâmetro "3 - valor"
    }

    public void setValor(int valor) {
        this.valor=valor;
    }

    public int getValor() {
        return this.valor;
    }

}
